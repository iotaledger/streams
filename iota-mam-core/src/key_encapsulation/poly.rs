#[cfg(test)]
mod test {
    use super::*;

    #[test]
    fn ntt_conv() {
        let mut f = Poly::new();
        let mut g = Poly::new();
        f.coeffs[0] = COEFF_ONE;
        g.coeffs[0] = COEFF_MINUS_ONE;
        let a = COEFF_ONE + COEFF_ONE;
        let b = COEFF_ONE + a;
        let c = COEFF_ONE + (a * b);

        for _ in 0..10 {
            let mut m = f;
            m.mul(&g);

            let mut tm = f;
            let mut tg = g;
            tm.ntt();
            tg.ntt();
            tm.conv(&tg);
            tm.intt();
            tg.intt();

            assert_eq!(m, tm);
            assert_eq!(g, tg);

            for i in 0..N {
                f.coeffs[i] = a + (b * f.coeffs[i]);
                g.coeffs[i] = b + (c * g.coeffs[i]);
            }
        }
    }

    #[test]
    fn ntt_ntt2() {
        let mut f = Poly::new();
        f.coeffs[0] = COEFF_ONE;
        let a = COEFF_ONE + COEFF_ONE;
        let b = COEFF_ONE + a;

        for _ in 0..10 {
            let mut t = f;
            t.ntt();
            let mut t2 = f;
            f.ntt2(&mut t2);
            assert_eq!(t, t2);

            t.intt();
            assert_eq!(f, t);
            let mut it2 = t2;
            t2.intt2(&mut it2);
            assert_eq!(t, it2);

            for i in 0..N {
                f.coeffs[i] = a + (b * f.coeffs[i]);
            }
        }
    }
}
